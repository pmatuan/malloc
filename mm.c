/*
 * mm.c - Dynamic Memory Allocator
 * 
 * This implementation uses an explicit free list with immediate coalescing.
 * Each block has a header and footer containing size and allocation status.
 * Free blocks are organized in a doubly-linked list for efficient management.
 * The allocator uses first-fit placement and immediate coalescing when freeing.
 */
